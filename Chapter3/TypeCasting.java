public class TypeCasting {
    public static void main(String[] args) {
        double d = 3;
        System.out.println("d is "+d);
        int i = (int) 3.0;
        System.out.println("i is "+i);
        double x = 4.5;
        int y = (int) x;
        System.out.println("the value of x: "+x);
        System.out.println("the value of y: "+y);
    
    }
    
}
